movies = {"Harry Potter", "Velozes e Furiosos"}

# Adicionar um elemento
movies.add("Sempre Ao Seu Lado")
print(movies)

# Remover um elemento
movies.remove("Sempre Ao Seu Lado")
print(movies)

# Atualizar mais de um elemento 
movies.update({"Star Wars", "Interestelar"})
print(movies)

# Observação: Não podemos adicionar valores iguais. 