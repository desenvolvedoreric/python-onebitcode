movies = ("Harry Potter", "Velozes e Furiosos", "Sempre Ao Seu Lado", "Star Wars")

# Buscar o primeiro elemento
print(movies[:1])

# Buscar o último elemento
print(movies[-1])

# Buscar de uma posição especifica
print(movies[2:])

# Buscar indice do elemento
print(movies.index("Velozes e Furiosos"))

# Observação: Não pode adicionar ou remover elementos e ordernar valores.