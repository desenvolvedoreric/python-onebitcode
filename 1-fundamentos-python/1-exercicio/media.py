# Desafio 2: Média de 4 notas
# Leia 4 notas
note1 = int(input("Digite sua primeira nota: "))
note2 = int(input("Digite sua segunda nota: "))
note3 = int(input("Digite sua terceira nota: "))
note4 = int(input("Digite sua quarta nota: "))
# Calcule a média
average = (note1 + note2 + note3 + note4) / 4
# Mostre em tela
print(f"Sua média foi: {average}")