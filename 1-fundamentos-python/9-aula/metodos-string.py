name = "Prince Of Persia"

# Deixa em maiuscula. 
print(name.upper()) 

# Deixa em minuscula.
print(name.lower())  

# Deixa primeira letra de cada palavra maiuscula.
print(name.capitalize())  

# Deixa primeira letra maiuscula. 
print(name.title()) 

# Centraliza palavra
print(name.center(20, "-"))  

# Retorna posição do caracter encontrado. 
print(name.find("a")) 

# Conta quantos caracteres contém na string. 
print(name.count("a")) 

# Troca um determinado valor da string
print(name.replace("Persia", "World")) 

# Quebra a string e transforma em uma lista.
print(name.split(" ")) 